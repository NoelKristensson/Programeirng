# Be användaren mata in basen för triangeln
bas = float(input("Ange basen för triangeln: "))

# Be användaren mata in höjden för triangeln
höjd = float(input("Ange höjden för triangeln: "))

# Beräkna arean av triangeln
area = (bas * höjd) / 2

# Visa resultaten
print(f"bas = {bas}")
print(f"höjd = {höjd}")
print(f"area = {area}")